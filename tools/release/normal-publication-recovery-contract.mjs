// The recovery reader fully paginates both exact-SHA inventories within this
// explicit ceiling. Reaching the ceiling fails closed; it never truncates to a
// latest-N window. Every HTTP attempt is journal-admitted by github-read.mjs.
export const NORMAL_PUBLICATION_RECOVERY_MAX_PAGES_PER_INVENTORY = 4;
export const NORMAL_PUBLICATION_RECOVERY_MAX_CANDIDATES = 4;
export const NORMAL_PUBLICATION_RECOVERY_MAX_ATTEMPTS = 2;
export const NORMAL_PUBLICATION_RECOVERY_READ_DEADLINE_MS = 20_000;
export const NORMAL_PUBLICATION_RECOVERY_DOWNLOAD_DEADLINE_MS = 45_000;

export const NORMAL_PUBLICATION_RECOVERY_GITHUB_CORE_REQUEST_MAX =
  (
    1
    + (2 * NORMAL_PUBLICATION_RECOVERY_MAX_PAGES_PER_INVENTORY)
    + NORMAL_PUBLICATION_RECOVERY_MAX_CANDIDATES
  ) * NORMAL_PUBLICATION_RECOVERY_MAX_ATTEMPTS;

export const NORMAL_PUBLICATION_RECOVERY_MAX_DURATION_MS =
  (
    1
    + (2 * NORMAL_PUBLICATION_RECOVERY_MAX_PAGES_PER_INVENTORY)
  ) * NORMAL_PUBLICATION_RECOVERY_READ_DEADLINE_MS
  + (
    NORMAL_PUBLICATION_RECOVERY_MAX_CANDIDATES
    * NORMAL_PUBLICATION_RECOVERY_DOWNLOAD_DEADLINE_MS
  );

export const NORMAL_PUBLICATION_RECOVERY_STEP_TIMEOUT_MS = 7 * 60_000;

if (NORMAL_PUBLICATION_RECOVERY_MAX_DURATION_MS >= NORMAL_PUBLICATION_RECOVERY_STEP_TIMEOUT_MS) {
  throw new Error("normal publication recovery network ceiling must leave time for ZIP validation and atomic restore");
}

const MINUTE_MS = 60_000;

// The root transport helper performs one exact-ref read, at most one
// non-replayed create, and at most one read-only ambiguous-result
// reconciliation. Its workflow step also covers bounded response parsing and
// local journal admission without pretending the mutation itself is retryable.
export const RELEASE_TRANSPORT_REF_MAX_CORE_REQUESTS = 3;
export const RELEASE_TRANSPORT_REF_STEP_TIMEOUT_MINUTES = 3;

// The inspector performs two immutable ZIP downloads, waits for the parent and
// its child-authorization artifact, and may make three standalone metadata
// reads (continuation artifact, lineage root, and current run). Keep these
// explicit so the workflow timeout cannot silently become shorter than an
// ambient retry configuration.
export const RELEASE_CONTINUATION_METADATA_READ_DEADLINE_MS = MINUTE_MS;
export const RELEASE_CONTINUATION_ARTIFACT_DOWNLOAD_DEADLINE_MS = 15 * MINUTE_MS;
export const RELEASE_CONTINUATION_PARENT_COMPLETION_DEADLINE_MS = 10 * MINUTE_MS;
export const RELEASE_CONTINUATION_AUTHORIZATION_DISCOVERY_DEADLINE_MS = 10 * MINUTE_MS;
export const RELEASE_CONTINUATION_STANDALONE_METADATA_READ_COUNT = 3;
export const RELEASE_CONTINUATION_PREPARATION_METADATA_READ_COUNT = 2;

export const RELEASE_CONTINUATION_INSPECTION_RETRY_ENVELOPE_MS =
  (2 * RELEASE_CONTINUATION_ARTIFACT_DOWNLOAD_DEADLINE_MS)
  + RELEASE_CONTINUATION_PARENT_COMPLETION_DEADLINE_MS
  + RELEASE_CONTINUATION_AUTHORIZATION_DISCOVERY_DEADLINE_MS
  + (
    RELEASE_CONTINUATION_STANDALONE_METADATA_READ_COUNT
    * RELEASE_CONTINUATION_METADATA_READ_DEADLINE_MS
  );

export const RELEASE_CONTINUATION_INSPECTION_LOCAL_MARGIN_MS = 2 * MINUTE_MS;
export const RELEASE_CONTINUATION_INSPECTION_STEP_TIMEOUT_MINUTES =
  (
    RELEASE_CONTINUATION_INSPECTION_RETRY_ENVELOPE_MS
    + RELEASE_CONTINUATION_INSPECTION_LOCAL_MARGIN_MS
  ) / MINUTE_MS;

export const RELEASE_CONTINUATION_PREPARATION_RETRY_ENVELOPE_MS =
  RELEASE_CONTINUATION_PREPARATION_METADATA_READ_COUNT
  * RELEASE_CONTINUATION_METADATA_READ_DEADLINE_MS;
export const RELEASE_CONTINUATION_PREPARATION_STEP_TIMEOUT_MINUTES = 3;

// A continuation dispatcher performs four standalone metadata reads (artifact,
// parent run, and the exact immutable transport ref before and after any
// not-before wait), one immutable artifact download, at most one bounded wait,
// and one non-replayed workflow-dispatch POST. Keep the full transport envelope
// below the step timeout, then reserve separate job time for pinned setup, the
// authorization upload, runner transitions, and cleanup.
export const RELEASE_CONTINUATION_DISPATCH_METADATA_READ_COUNT = 4;
export const RELEASE_CONTINUATION_DISPATCH_DELAY_DEADLINE_MS = 15 * MINUTE_MS;
export const RELEASE_CONTINUATION_DISPATCH_REQUEST_DEADLINE_MS = 2 * MINUTE_MS;
export const RELEASE_CONTINUATION_DISPATCH_RETRY_ENVELOPE_MS =
  (
    RELEASE_CONTINUATION_DISPATCH_METADATA_READ_COUNT
    * RELEASE_CONTINUATION_METADATA_READ_DEADLINE_MS
  )
  + RELEASE_CONTINUATION_ARTIFACT_DOWNLOAD_DEADLINE_MS
  + RELEASE_CONTINUATION_DISPATCH_DELAY_DEADLINE_MS
  + RELEASE_CONTINUATION_DISPATCH_REQUEST_DEADLINE_MS;
export const RELEASE_CONTINUATION_DISPATCH_LOCAL_MARGIN_MS = 4 * MINUTE_MS;
export const RELEASE_CONTINUATION_DISPATCH_STEP_TIMEOUT_MINUTES =
  (
    RELEASE_CONTINUATION_DISPATCH_RETRY_ENVELOPE_MS
    + RELEASE_CONTINUATION_DISPATCH_LOCAL_MARGIN_MS
  ) / MINUTE_MS;
export const RELEASE_CONTINUATION_DISPATCH_CHECKOUT_TIMEOUT_MINUTES = 3;
export const RELEASE_CONTINUATION_DISPATCH_NODE_TIMEOUT_MINUTES = 3;
export const RELEASE_CONTINUATION_AUTHORIZATION_UPLOAD_TIMEOUT_MINUTES = 5;
export const RELEASE_CONTINUATION_DISPATCH_JOB_MARGIN_MINUTES = 9;
export const RELEASE_CONTINUATION_DISPATCH_JOB_TIMEOUT_MINUTES =
  RELEASE_CONTINUATION_DISPATCH_CHECKOUT_TIMEOUT_MINUTES
  + RELEASE_CONTINUATION_DISPATCH_NODE_TIMEOUT_MINUTES
  + RELEASE_CONTINUATION_DISPATCH_STEP_TIMEOUT_MINUTES
  + RELEASE_CONTINUATION_AUTHORIZATION_UPLOAD_TIMEOUT_MINUTES
  + RELEASE_CONTINUATION_DISPATCH_JOB_MARGIN_MINUTES;

if (
  !Number.isSafeInteger(RELEASE_CONTINUATION_INSPECTION_STEP_TIMEOUT_MINUTES)
  || !Number.isSafeInteger(RELEASE_CONTINUATION_DISPATCH_STEP_TIMEOUT_MINUTES)
  || !Number.isSafeInteger(RELEASE_CONTINUATION_DISPATCH_JOB_TIMEOUT_MINUTES)
  || RELEASE_CONTINUATION_PREPARATION_STEP_TIMEOUT_MINUTES * MINUTE_MS
    < RELEASE_CONTINUATION_PREPARATION_RETRY_ENVELOPE_MS
  || RELEASE_CONTINUATION_DISPATCH_STEP_TIMEOUT_MINUTES * MINUTE_MS
    < RELEASE_CONTINUATION_DISPATCH_RETRY_ENVELOPE_MS
) {
  throw new Error("release continuation workflow timeouts must cover their exact read envelopes");
}
